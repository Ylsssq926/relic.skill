type DeepStringify<T> = T extends string
  ? string
  : T extends number
    ? number
    : T extends boolean
      ? boolean
      : T extends (...args: infer A) => infer R
        ? (...args: A) => DeepStringify<R>
        : T extends readonly (infer U)[]
          ? DeepStringify<U>[]
          : { [K in keyof T]: DeepStringify<T[K]> };

export type Dictionary = DeepStringify<typeof zh>;

export default zh;
